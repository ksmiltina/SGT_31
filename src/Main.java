public class Main {
    public static void main(String[] args) {
        // \t first line space, \n enter (extra line)
        System.out.println("\tHello World!");
        System.out.print("Hello World!\n");

        int x;
        x = 15;
        System.out.println(x);

        int y = 10;
        System.out.println("y = " + y);

        //string data type
        String name = "Kristiana";
        System.out.println(name);

        //double data type, can do math
        double temperature = 36.6d;
        System.out.println(temperature);

        String temperature2 = "36.6"; //cannot do math
        System.out.println(temperature2);

        //final variable - cannot change the value
        final double pie;
        pie = 3.1415346654569876d;

    }
}
