public class MathClass {
    public static void main(String[] args) {

        float num = 5.54f;

        System.out.println(num);
        System.out.println(Math.floor(num)); //rounding down
        System.out.println(Math.ceil(num)); //rounding up
        System.out.println(Math.round(num)); //rounding according to math principles
        System.out.println(Math.abs(num)); //the absolute value (how far from zero, if negative then without the minus sign)
        System.out.println(Math.pow(7,3)); //power = pakaape, COMMA
        System.out.println(Math.sqrt(64)); //square root, sakne
        System.out.println(Math.max(100,103)); //max value between two
        System.out.println(Math.min(100,103)); //min value between two
        System.out.println(Math.random() * 10); //random value
        System.out.println((int) 99.989); //casting / making the number int
        System.out.println((int)(Math.random() * 10)); //random value integer
        System.out.println((10 + 5) * 2);





    }
}
